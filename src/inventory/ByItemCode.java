public void retrieve(){
    try{
        HashMap b = new HashMap();
         b.put("ItemCode", jTextField1.getText());
         
        jPanel2.removeAll();
        jPanel2.repaint();
        jPanel2.revalidate();
        
        JasperDesign Jdesign = JRXmlLoader.load("C:\\Inventory\\src\\inventory\\ItemcodeHistory.jrxml");
        JasperReport Jreport = JasperCompileManager.compileReport(Jdesign);
        JasperPrint Jprint = JasperFillManager.fillReport(Jreport, b, connec);
        
        JRViewer w = new JRViewer(Jprint);
        jPanel2.setLayout(new BorderLayout());
        jPanel2.add(w);
    }   catch (JRException ex) {
            Logger.getLogger(ByItemCode.class.getName()).log(Level.SEVERE, null, ex);
        }
}
